%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([]).
-export([start_link/0]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link()->
	supervisor:start_link({local,?SPOUTS_SUP}, ?SPOUTS_SUP, []).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
	error_logger:info_msg("Initial ~p~n", [?SPOUTS_SUP]),
    SpoutActorServer = {?SPOUT_ACTOR,{?SPOUT_ACTOR,start_link,[]},
	                permanent,2000,worker,[?SPOUT_ACTOR]},	
    {ok,{{simple_one_for_one,1,1}, [SpoutActorServer]}}.

