-module(sm_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------
-include("../include/sm.hrl").
%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([]).
-export([start_link/0]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).


%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link() ->
	supervisor:start_link({local,?MODULE}, ?MODULE, []).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
	error_logger:info_msg("Initial ~p~n", [?SM_SUP]),
	Hook_sup = {?HOOK_SUP,{?HOOK_SUP,start_link,[]},
	          permanent,infinity,supervisor,[]},
    Spouts_sup = {?SPOUTS_SUP,{?SPOUTS_SUP,start_link,[]},
	          permanent,infinity,supervisor,[]},
	Bolts_sup = {?BOLTS_SUP,{?BOLTS_SUP,start_link,[]},
	          permanent,infinity,supervisor,[]},
    {ok,{{one_for_all,1,1}, [Spouts_sup, Bolts_sup,Hook_sup]}}.

